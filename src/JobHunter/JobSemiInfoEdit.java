    public void setAllInfo() {

        jLabelBackground.setIcon(Functions.setIconImage("src\\MainPackage\\profile.jpg", jLabelBackground.getWidth(), jLabelBackground.getHeight()));
        jLabelBackground.setText("");

        this.setIconImage(new ImageIcon("src\\MainPackage\\profile.jpg").getImage());

        jLabelAccountId.setText("Account ID: " + mainIndex);

        try {

            PreparedStatement ps = setAllConnections.getConAdmin().prepareStatement("SELECT * FROM hunters_account WHERE id = " + mainIndex);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                jTextFieldUsername.setText(rs.getString(2));
                jTextFieldPassword.setText(rs.getString(3));
                jTextFieldFirstName.setText(rs.getString(5));
                jTextFieldMiddleName.setText(rs.getString(6));
                jTextFieldLastName.setText(rs.getString(7));
                jTextFieldSuffix.setText(rs.getString(8));
                jTextFieldAge.setText(String.valueOf(rs.getInt(9)));

                if (rs.getInt(10) == 0) {
                    jLabelStatus.setText("STATUS - NOT HIRED");
                    jLabelJobName.setText("");
                    jLabelJobIndustry.setText("");
                    jLabelRecruitersName.setText("");
                } else {
                    jLabelStatus.setText("STATUS - HIRED");
                    jLabelJobName.setText(rs.getString(11));
                    jLabelJobIndustry.setText(rs.getString(12));
                    jLabelRecruitersName.setText(rs.getString(13));

                    jButtonResign.setEnabled(true);
                }

            } else {
                JOptionPane.showMessageDialog(null, "No Data Found");

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }


    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed

        JOptionPane.showMessageDialog(null, "Note that changes to these data cannot be undone.");

        int response = JOptionPane.showConfirmDialog(null, "Do you want to proceed Updating the data?", "Update Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (response == 0) {
            try {

                PreparedStatement ps = setAllConnections.getConAdmin().prepareStatement("UPDATE hunters_account SET username = ?, password = ?, first_name = ?, middle_name = ?, last_name = ?, suffix = ?, age = ? WHERE id = " + mainIndex);
                ps.setString(1, jTextFieldUsername.getText());
                ps.setString(2, jTextFieldPassword.getText());
                ps.setString(3, jTextFieldFirstName.getText());
                ps.setString(4, jTextFieldMiddleName.getText());
                ps.setString(5, jTextFieldLastName.getText());
                ps.setString(6, jTextFieldSuffix.getText());
                ps.setInt(7, Integer.parseInt(jTextFieldAge.getText()));
                ps.executeUpdate();

                JOptionPane.showMessageDialog(null, "Updated Successfully");

                setAllInfo();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed


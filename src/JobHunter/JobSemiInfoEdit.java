    private void jButtonResignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonResignActionPerformed

        int response = JOptionPane.showConfirmDialog(null, "Are you sure you want to Resign?", "Resign Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (response == 0) {
            try {
                PreparedStatement ps = setAllConnections.getConAdmin().prepareStatement("UPDATE hunters_account SET status = 0, job_hired = DEFAULT, job_industry = DEFAULT, recruiter_id = DEFAULT WHERE id = " + mainIndex);
                ps.executeUpdate();

                JOptionPane.showMessageDialog(null, "Resigned Successfully");
                jButtonResign.setEnabled(false);
                setAllInfo();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }

    }//GEN-LAST:event_jButtonResignActionPerformed


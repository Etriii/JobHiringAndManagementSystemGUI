    public void setAllInfo() {
        try {
            PreparedStatement ps = setAllConnections.getConAdmin().prepareStatement("SELECT * FROM recruiters_account WHERE id = " + mainIndex);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                jLabelRecruitersID.setText("Recruiters ID: " + mainIndex);
                jTextFieldUsername.setText(rs.getString(2));
                jTextFieldPassword.setText(rs.getString(3));
                jTextFieldGivenName.setText(rs.getString(5));
                jTextFieldFamilyName.setText(rs.getString(6));
                jTextFieldEmail.setText(rs.getString(7));
                jTextFieldBusinessName.setText(rs.getString(8));
                jTextFieldLastName2.setText(rs.getString(9));
                jTextFieldLastName4.setText(rs.getString(10));

            } else {
                JOptionPane.showMessageDialog(null, "No Data Found");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }



    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed

        JOptionPane.showMessageDialog(null, "Note that changes to these data cannot be undone.");

        int response = JOptionPane.showConfirmDialog(null, "Do you want to proceed Updating the data?", "Update Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (response == 0) {
            try {

                PreparedStatement ps = setAllConnections.getConAdmin().prepareStatement("Update recruiters_account SET username = ?, password = ?, given_name = ?, family_name = ?, e_mail = ?, business_name = ?, phone_number = ?, complete_address = ? WHERE id = " + mainIndex);
                ps.setString(1, jTextFieldUsername.getText());
                ps.setString(2, jTextFieldPassword.getText());
                ps.setString(3, jTextFieldGivenName.getText());
                ps.setString(4, jTextFieldFamilyName.getText());
                ps.setString(5, jTextFieldEmail.getText());
                ps.setString(6, jTextFieldBusinessName.getText());
                ps.setString(7, jTextFieldLastName2.getText());
                ps.setString(8, jTextFieldLastName4.getText());
                ps.executeUpdate();

                JOptionPane.showMessageDialog(null, "Updated Successfully");

                setAllInfo();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed

